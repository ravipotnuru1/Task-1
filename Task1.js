// 49. Write rules of var.

// Rules of var
// 1. Declaration:
        // Declaration is creating or naming a  var variable(Memory).
        // var p

// 2. Initialization:
        // Intialization is assigning data to the declared var variable.
        // p = 10

// 3. Declaration + Initialization
        // Declaration and initialization of var variable can be done at a time in single line.
        // var p=10

// 4. Reintialization
        // we can reintialize the var variable to update the data.
        // p =100 --> updates the value

// 5. ReDeclaration
        // We can Redeclare the var variable to update the data.
        // var p = 200 ----> updates the value.

